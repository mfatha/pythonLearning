#Statistics (MEAN, STANDARD DEVIATION , VARIENT)
import statistics

list = [1,4,5,6,7,8,2,3,9,2,3,4,5,6,1,9]

#mean
x= statistics.mean(list)

print(x)

#median
x= statistics.median(list)

print(x)

#standard deviation
x= statistics.stdev(list)

print(x)

#varience
x= statistics.variance(list)

print(x)


